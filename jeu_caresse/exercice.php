 Exercice :

Créer un mini de jeu de caresse en :
- Modélisant des personnages qui ont une
force de caresse de 0 à 100.
initialisation aléatoire entre 0 et 100

- qui ont une douceur de 0 à 500. initialisation à 0
- qui ont un nom

- une méthode caresser, qui va donc augmenter la douceur
d'un autre personnage
de la valeur se situant entre la moitié et la totalité de
sa force de caresse
ex : force de 80 : caresse entre 40 et 80

- une méthode caresserSurement qui caresse toujours 7.5/10
de la force de caresse

- le jeu se termine quand une personne
est trop douce : douceur à 500


